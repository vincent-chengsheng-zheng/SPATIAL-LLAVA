"""
core/data/refcoco_loader.py

RefCOCO dataset loader for Spatial-LLaVA training.

All data is loaded from pre-processed pickle files in SharedFolder.
Run pipeline/stage_1_data_preparation.py first to generate these files.

Usage:
    from core.data.refcoco_loader import RefCOCODataset

    dataset = RefCOCODataset(
        pkl_path="~/SharedFolder/data/refcoco_train.pkl",
        split="train"
    )
    sample = dataset[0]
    # sample = {
    #     "image":  Tensor(3, 384, 384),
    #     "input_ids": Tensor(77,),
    #     "bbox":   Tensor(4,)   — [x_center, y_center, w, h] normalized
    # }

    loader = dataset.get_dataloader(batch_size=4, shuffle=True)
"""

import os
from torch.utils.data import Dataset, DataLoader
from torch import Tensor
from typing import Dict, Optional


class RefCOCODataset(Dataset):
    """
    PyTorch Dataset for the pre-processed RefCOCO dataset.

    Loads from pickle files generated by stage_1_data_preparation.py.
    Each sample contains a preprocessed image tensor, tokenized text,
    and a normalized bounding box.

    Args:
        pkl_path : Path to the pickle file
                   (e.g. ~/SharedFolder/data/refcoco_train.pkl)
        split    : One of "train", "val", "test" — used for logging only
    """

    def __init__(self, pkl_path: str, split: str = "train"):
        self.split = split
        self.pkl_path = os.path.expanduser(pkl_path)
        self.data = self._load(self.pkl_path)

    def _load(self, path: str) -> list:
        """
        Load and return the list of samples from a pickle file.

        Args:
            path : Absolute path to the .pkl file

        Returns:
            List of dicts, each containing image, input_ids, bbox

        Raises:
            FileNotFoundError : If the pickle file does not exist
        """
        raise NotImplementedError(
            "Member 2: implement _load(). "
            "Open the pkl file, verify it has the expected keys, "
            "and return the list of samples."
        )

    def __len__(self) -> int:
        """Return total number of samples in this split."""
        raise NotImplementedError("Member 2: return len(self.data)")

    def __getitem__(self, idx: int) -> Dict[str, Tensor]:
        """
        Return a single sample as a dict of tensors.

        Args:
            idx : Sample index

        Returns:
            {
                "image":     Tensor of shape (3, 384, 384) — normalized RGB
                "input_ids": Tensor of shape (77,)         — tokenized prompt
                "bbox":      Tensor of shape (4,)          — [x, y, w, h] in [0, 1]
            }
        """
        raise NotImplementedError(
            "Member 2: fetch self.data[idx] and return the three tensors. "
            "Make sure dtypes are: image=float32, input_ids=long, bbox=float32."
        )

    def get_dataloader(
        self,
        batch_size: int = 4,
        shuffle: bool = True,
        num_workers: int = 2,
        pin_memory: bool = True,
    ) -> DataLoader:
        """
        Return a DataLoader for this dataset.

        Args:
            batch_size  : Samples per batch (default: 4)
            shuffle     : Shuffle every epoch (default: True for train)
            num_workers : Parallel data loading workers (default: 2)
            pin_memory  : Speed up GPU transfer (default: True)

        Returns:
            torch.utils.data.DataLoader
        """
        return DataLoader(
            self,
            batch_size=batch_size,
            shuffle=shuffle,
            num_workers=num_workers,
            pin_memory=pin_memory,
        )

    @classmethod
    def from_config(cls, config: dict, split: str) -> "RefCOCODataset":
        """
        Construct dataset from a config dict (loaded from YAML).

        Args:
            config : Dict with key "data_dir" pointing to SharedFolder/data/
            split  : "train", "val", or "test"

        Returns:
            RefCOCODataset instance

        Example:
            config = {"data_dir": "~/SharedFolder/data/"}
            train_set = RefCOCODataset.from_config(config, split="train")
        """
        data_dir = os.path.expanduser(config["data_dir"])
        pkl_path = os.path.join(data_dir, f"refcoco_{split}.pkl")
        return cls(pkl_path=pkl_path, split=split)


def load_splits(
    data_dir: str,
    splits: Optional[list] = None,
) -> Dict[str, RefCOCODataset]:
    """
    Convenience function to load multiple splits at once.

    Args:
        data_dir : Path to the directory containing pkl files
        splits   : List of splits to load (default: ["train", "val", "test"])

    Returns:
        Dict mapping split name → RefCOCODataset

    Example:
        datasets = load_splits("~/SharedFolder/data/")
        train_loader = datasets["train"].get_dataloader(batch_size=4)
    """
    if splits is None:
        splits = ["train", "val", "test"]

    data_dir = os.path.expanduser(data_dir)
    return {
        split: RefCOCODataset(
            pkl_path=os.path.join(data_dir, f"refcoco_{split}.pkl"),
            split=split,
        )
        for split in splits
    }
